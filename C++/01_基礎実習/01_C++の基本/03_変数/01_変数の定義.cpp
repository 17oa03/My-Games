/*
【プログラム概要】
変数を定義して、演算結果を出力する。
*/

//【プログラム】
#include <iostream>
using namespace std;

int main(void) {
	int a, b;

	a = 1;
	cout << "a = " << a << endl;

	b = 5;
	cout << "2b = " << b * 2 << endl;
}

/*
【実行結果】
a = 1
2b = 10
*/

/*
【考察】
変数は何かしらの値を入れておけるもの。
変数は定義してから使う。
変数へは=を使って値を入れることができる。
変数名を書けば変数に入っている値の代わりになる。
*/