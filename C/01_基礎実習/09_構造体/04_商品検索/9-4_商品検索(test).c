/*
【考察】
構造体のポインタ変数を宣言、
for文でアドレスをセット、
条件文に「p_data < data + ITEMS(商品の数)」で
「ITEMS」先のアドレスを指定しているので、
ポインタ変数のアドレスがインクリメントされて
比較しているアドレスに到達するまでループされる。

構造体のポインタ変数から
メンバ変数の参照をするには、
「->(アロー)」演算子を使う。
*/